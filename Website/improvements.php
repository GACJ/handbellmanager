<h2>Improving the Experience</h2>

<p>Although the ActionXL wired motion controllers work very well with Abel, some people may find that they are rather small and light,
as they do not have the weight or feel of ringing real handbells. Below are the results of some experiments conducted by Richard
Johnston and Simon Humphrey to make these controllers into more realistic dummy handbells. If you have ideas for improvements, please send
them to us, and we will include them on this page.</p>

<p>The ActionXL "FriiHand" wireless controllers on the other hand are larger and heavier. Although the balance of weight is not like 
a real handbell, it is a closer match, so modification is less likely to be considered necessary or easy to achieve.</p> 

<p>The lack of weight of the ActionXL wired controllers means the ringer needs good fine control of his or her arm muscles, which some people find difficult. 
There is no handbell weight to provide the inertia that creates a physical sense of rhythm.
The balance is wrong too - real handbells project beyond the ringers hands and this too helps create the feeling of weight. Finally, there
is no physical feedback from the sensors to the hands as the clapper hits the bell.</p>

<p>Just adding a one ounce weight to the sensor and holding the sensor a bit nearer to the wire makes a surprising improvement to the balance.
In figure 1, a piece of lead solder was taped to the sensor, and the unit held as shown.</p>

