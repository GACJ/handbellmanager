<p>Neither design provides the clunk into the hand as the clapper hits the bell, which some people find useful for their striking.
Could a dummy clapper be added without adversely affecting the operation of the sensor?  The dummy handbell (Figure 3) was hastily
and inelegantly constructed, which proved that clapper feel could be created without causing sensor problems.  But this design was
difficult to make.  Something simpler was needed.</p>

<div align=center>
<p><img src="img/figure3.jpg" alt="Figure 3" width="400" height="280" hspace="15"></p>
<p class="caption">Figure 3 - A dummy clapper assembly</p>
</div>

<p>The impact feel was produced much more simply by developing Simon Humphrey's design (The side view is Figure 4).  The handle is
a rectagular piece of 9mm (3/8") plywood, about 25mm (1") wide and 200mm (8") long.  Two holes were drilled close to one end to take
bolts of about 9mm (3/8") diameter.  In the end one was placed a steel bolt of total length about 80mm (3"), with a long unthreaded shank.
On this bolt is a leather washer, then a weight (a bronze bush was used in the photo but an oversize nut will do, provided the threads do
not catch on the bolt) which emulates the clapper, another leather washer, then nuts on each side of the handle, which are tightened up.
The second bolt is provided to provide extra weight representing the bell and balances the assembly on the handle.
None of the dimensions are critical, so this can be made from oddments of scrap.  The sensor is taped to the handle. A slot could be cut
in the user end of the handle to hold the sensor wire, or the wire simply taped to the handle for greater security.</p>

<div align=center>
<p><img src="img/figure4.jpg" alt="Figure 4" width="288" height="400" hspace="15"></p>
<p class="caption">Figure 4 - Adjustable design</p>
</div>

<p>Adjustments: The length of the bolt on which the clapper weight slides affects how soon the "clapper" hits after the dummy handbell is
rung. A shorter bolt results in the "clapper" hitting earlier in the swing, a longer one later, so it is possible to adjust the strike.
Odd struckness can be addressed by adjusting the other bolt nearer to balance or further out.  The weight of the clapper weight affects
how much clapper response is provided - more weight and longer travel produces a greater impulse.  The leather washers reduce the rattle
sound made by the weight when it hits the ends to an acceptable level, and also make the impulse felt more realistic.  The overall balance
of the bell is achieved by moving the sensor up and down the handle until the balance feels right.</p>

<p>This dummy handbell shown in Figure 4 gives a satisfactory thud in time with the Abel's bell sound and Handbell Manager's standard
calibration settings, and has been tested as a reasonable approximation to the weight and dynamic feel of a 14D handbell.</p>

<p>Of course, one other option is to attach the controller to an old handbell with its clapper leathers or assembly removed. If you have any other ideas, please let us know. 
You can send an email by clicking on the name in the footer.<p>

