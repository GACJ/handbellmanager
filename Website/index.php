<p>There is no computer keyboard or mouse interaction during the video - the controllers are swung to ring two bells, and buttons on the controllers are used to start the others ringing, initiate "Go Bristol" and call the three bobs.</p>

<h3>What do you need?</h3>
<ul>
<li>Two ActionXL USB motion controllers (available from <a target="_top"
href="http://www.actionxl.com">www.actionxl.com</a>
at $19.95 each for the wired model and $39.95 each for the wireless model. 
The wired model is also available from <a target="_top"
href="http://www.amazon.co.uk">www.amazon.co.uk</a> at only &pound;4.95 each.
Prices are as quoted by the respective websites on 1 August 2009)</li>
<li>Abel version 3.5 or later (from <a target="_top"
href="http://www.abelsim.co.uk">www.abelsim.co.uk</a>)</li>
<li>Handbell Manager (from <a href="http://sourceforge.net/projects/handbellmanager">Sourceforge</a>)</li>
<li>A PC with two spare USB ports, Microsoft Windows and .Net Framework (from <a target="_top"
href="http://www.microsoft.com">www.microsoft.com</a>)</li>
</ul>

