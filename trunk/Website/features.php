<h2>Features</h2>
<p>Below are some of the features to be developed in the Composition Library software.</p>

<ol>
<strong><li>Method</li></strong>
<ul>
<li>Replicate the online method database on the client PC and keep up-to-date</li>
<li>Find methods matching search criteria and select a method</li>
<li>Derive and display method properties</li>
<li>Display method grid/blue line</li>
<li>Input a new method and keep on the local database until rung</li>
<li>Match online updates and remove rung methods</li>
<li>Edit methods input to local library</li>
<li>Protect the integrity of downloaded methods by disallowing edit</li>
<li>Find methods true to a chosen composition</li>
</ul>
<strong><li>Composition</li></strong>
<ul>
<li>Replicate the online composition database on the client PC and keep up-to-date</li>
<li>Find compositions matching search criteria and select a composition</li>
<li>Find true compositions for the selected method</li>
<li>Derive and display composition properties</li>
<li>Display a composition in standard format, compact or expanded</li>
<li>Allow drilldown for courseheads, leadheads and rows</li>
<li>Check that compositions are true to the selected method, are of the expected length, and come round, by verifying and displaying this information when the composition is formatted</li>
<li>Display and compare musical characteristics</li>
<li>Input a new composition to the local database pending upload to the online database</li>
<li>Amend/delete compositions from the online database, if the user has the appropriate authority</li>
<li>Upload compositions, removing from local database</li>
<li>Print a composition, with a blue line and/or grid if desired</li>
<li>Find trivial variations of compositions, such as rotations, inversions, and obvious rearrangements</li>
</ul>
<strong><li>Music Scoring</li></strong>
<ul>
<li>Maintain user-definable masks by stage for row comparison and score for each mask</li>
<li>Select which masks are active for composition analysis</li>
<li>Allow mask types for row, backstroke row, handstroke row, leadhead row, coursehead row, wrap etc</li>
<li>Subtotal groups of masks</li>
<li>Calculate a total score for each composition being compared</li>
</ul>
<strong><li>Collection maintenance</li></strong>
<ul>
<li>Enter details of composition collections e.g. to allow the user to compile a new collection, or to catalogue a published collection</li>
<li>Assign compositions to collections</li>
<li>Display a list of compositions in a collection</li>
<li>Display the number of compositions in each collection</li>
<li>Display or print a collection</li>
</ul>
<strong><li>Statistics</li></strong>
<ul>
<li>Display statistics such as:-</li>
<ul>
<li>The number of methods in the online and local method library by stage, class and leadhead group</li>
<li>The number of compositions in composition library and the number pending upload</li>
<li>The number of compositions uploaded/amended by user name</li>
<li>The number of compositions added by month</li>
<li>The update log for the online composition database</li>
<li>The update log for the local database</li>
</ul>
<li>Maintain an online submission log for the online database</li>
<li>Maintain an update log for the local database</li>
</ul>
<strong><li>User</li></strong>
<ul>
<li>Initial registration and verification</li>
<li>Sign on</li>
<li>Password change</li>
<li>Password reset</li>
<li>Email address change</li>
<li>Set admin rights</li>
</ul>
</ol>

